//------------------------------------------------------------------------------------------------
// $(selector).em()
//------------------------------------------------------------------------------------------------

(function ($) {
    $.fn.em = function () {
        var em = parseFloat(this.css('font-size'));
        return em;
    };
})(jQuery);


//------------------------------------------------------------------------------------------------
// $(selector).emWidth()
// $(selector).emWidth(value)
//------------------------------------------------------------------------------------------------

(function ($) {
    $.fn.emWidth = function (value) {
        if (typeof (value) === 'undefined' || !value) {
            var em = this.em();
            var width = this.width();
            return (width / em);
        }
        else {
            this.css('width', (value + 'em'));
        }
    };
})(jQuery);


//------------------------------------------------------------------------------------------------
