//------------------------------------------------------------------------------------------------
// $.em()
//------------------------------------------------------------------------------------------------

(function ($) {
    $.em = function () {
        var em = parseFloat($('body').css('font-size'));
        return em;
    };
})(jQuery);


//------------------------------------------------------------------------------------------------
// $.emWindowWidth()
//------------------------------------------------------------------------------------------------

(function ($) {
    $.emWindowWidth = function () {
        var em = $.em();
        var width = $(window).width();
        return (width / em);
    };
})(jQuery);


//------------------------------------------------------------------------------------------------
// $.emWindowHeight()
//------------------------------------------------------------------------------------------------

(function ($) {
    $.emWindowHeight = function () {
        var em = $.em();
        var height = $(window).height();
        return (height / em);
    };
})(jQuery);


